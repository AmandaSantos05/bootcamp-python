# Cria um novo conjunto contendo apenas os elementos que estão em ambos os conjuntos.
# Use o método intersection() ou o operador &

conjunto_a = {1, 2, 3}
conjunto_b = {2, 3, 4}

resultado = conjunto_a.intersection(conjunto_b)
print(resultado)

resultado2 = conjunto_a & conjunto_b
print(resultado2)